
s='sumit raut is there'

print(s.find('r')) #6

print(s.capitalize()) #Sumit raut is there

print(s.upper()) #SUMIT RAUT IS THERE

print(s.count('r')) #r

print(s.isalnum()) #False

print(s.isalpha()) #False

print(s.isdigit()) #False

print(s.isspace())

print(s.split(' '))  #['sumit', 'raut', 'is', 'there']

print(s.partition('s')) #('', 's', 'umit raut is there')